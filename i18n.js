const SUPPORTED = LANGUAGES.map(l => l.code);

function resolveLang() {
  const saved = localStorage.getItem("lang");
  if (SUPPORTED.includes(saved)) return saved;
  // Detect from browser preferred languages (e.g. ja-JP -> ja)
  const prefs = navigator.languages && navigator.languages.length ? navigator.languages : [navigator.language || "en"];
  for (const p of prefs) {
    const code = (p || "").toLowerCase().split("-")[0];
    if (SUPPORTED.includes(code)) return code;
  }
  return "en";
}

function applyLang(lang) {
  if (!SUPPORTED.includes(lang)) lang = "en";
  const dict = locales[lang] || locales.en;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (dict[key] != null) el.textContent = dict[key];
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.dataset.i18nHtml;
    if (dict[key] != null) el.innerHTML = dict[key];
  });

  // Metadata
  if (dict["meta.title"]) document.title = dict["meta.title"];
  const setMeta = (sel, attr, key) => {
    const el = document.querySelector(sel);
    if (el && dict[key] != null) el.setAttribute(attr, dict[key]);
  };
  setMeta('meta[name="description"]', "content", "meta.description");
  setMeta('meta[property="og:title"]', "content", "meta.ogTitle");
  setMeta('meta[property="og:description"]', "content", "meta.ogDescription");

  // Update theme toggle aria-label for current language
  const themeBtn = document.querySelector("[data-theme-toggle]");
  if (themeBtn) {
    const isDark = document.documentElement.getAttribute("data-theme") === "dark";
    themeBtn.setAttribute("aria-label", isDark ? dict["nav.themeToLight"] : dict["nav.themeToDark"]);
  }

  // Keep switcher in sync
  const sel = document.querySelector("[data-language-select]");
  if (sel) sel.value = lang;

  localStorage.setItem("lang", lang);
}

// Build the language switcher options
function buildSwitcher() {
  const sel = document.querySelector("[data-language-select]");
  if (!sel) return;
  if (sel.options.length) return;
  LANGUAGES.forEach(l => {
    const o = document.createElement("option");
    o.value = l.code; o.textContent = l.name;
    sel.appendChild(o);
  });
}

document.addEventListener("DOMContentLoaded", () => {
  buildSwitcher();
  const lang = resolveLang();
  applyLang(lang);
  const sel = document.querySelector("[data-language-select]");
  if (sel) {
    sel.value = lang; // sync dropdown to the active language
    sel.addEventListener("change", e => {
      applyLang(e.target.value);
      sel.value = e.target.value; // keep in sync after manual switch
    });
  }
});

// Re-sync theme aria-label when theme toggles (theme script runs first)
document.addEventListener("click", e => {
  const t = e.target.closest("[data-theme-toggle]");
  if (!t) return;
  setTimeout(() => {
    const lang = localStorage.getItem("lang") || "en";
    const dict = locales[lang] || locales.en;
    const isDark = document.documentElement.getAttribute("data-theme") === "dark";
    t.setAttribute("aria-label", isDark ? dict["nav.themeToLight"] : dict["nav.themeToDark"]);
  }, 0);
});
